"""GitHub code-search query catalog for mnemonic exposure hunting.

Two complementary strategies:

1. **Code constructs** — API / env / library patterns that almost always sit
   next to a real seed in application code (higher precision).

2. **BIP39 word n-grams** — short phrases of uncommon wordlist words, often
   only appear when someone pasted a full mnemonic (higher recall, more noise).

GitHub code search is rate-limited; keep the default catalog focused.
"""

