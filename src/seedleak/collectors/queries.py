def build_bip39_ngram_queries(
    *,
    n: int = 3,
    count: int = 40,
    stride: int = 3,
) -> list[SearchQuery]:
    """Build quoted n-gram queries from BIP39 words + wallet context.

    Example: ``"satoshi scale scene" (mnemonic OR seed OR wallet)``

    These are **not** full mnemonics — just bait phrases that often sit
    inside a pasted 12/24-word string. Exclude pure wordlist files via -path.
    """
    words = list(_ngram_source_words())
    if len(words) < n:
        return []
    out: list[SearchQuery] = []
    i = 0
    while len(out) < count and i + n <= len(words):
        gram = " ".join(words[i : i + n])
        # Require wallet-ish context; drop obvious wordlist paths when possible
        # Keep query simple for GitHub code search (no paren groups → fewer 422s)
        q = f'"{gram}" mnemonic OR seed OR wallet OR bip39'
        out.append(
            SearchQuery(
                q,
                "bip39_ngram",
                f"{n}-gram from BIP39 wordlist offset={i}",
            )
        )
        i += stride
    return out


def default_hunt_queries(
    *,
    include_code: bool = True,
    include_env: bool = True,
    include_keyword: bool = True,
    include_ngrams: bool = True,
    ngram_count: int = 40,
    ngram_n: int = 3,
) -> list[SearchQuery]:
    """Full default catalog for a max-power hunt."""
    qs: list[SearchQuery] = []
    if include_code:
        qs.extend(CODE_CONSTRUCT_QUERIES)
    if include_env:
        qs.extend(ENV_QUERIES)
    if include_keyword:
        qs.extend(KEYWORD_QUERIES)
    if include_ngrams:
        qs.extend(
            build_bip39_ngram_queries(n=ngram_n, count=ngram_count)
            + build_bip39_ngram_queries(n=4, count=max(10, ngram_count // 2), stride=5)
        )
    # Dedupe by query string
    seen: set[str] = set()
    uniq: list[SearchQuery] = []
    for q in qs:
        if q.query in seen:
            continue
        seen.add(q.query)
        uniq.append(q)
    return uniq


def queries_as_strings(items: list[SearchQuery] | None = None) -> list[str]:
    items = items or default_hunt_queries()
    return [q.query for q in items]
